package com.example.accessmodifiers;

/**
 * Class to demonstrate access modifiers in Java i.e private, default, protected, public
 */
public class AccessModifiersClass {
    /*
       Member variables are in order of most restrictive to least restrictive.
     */
    private String name; //private access modifier
    int age; // default access modifier
    protected int height; // protected access modifier
    public String address; // public access modifier
}
